var side = 20;
var grassArr = [];
var xotakerArr = [];
var gishatichArr = [];
var vorsordArr = [];
var djoxqArr = [];
var weather = 0;

function setup() {
    frameRate(4);
    createCanvas(matrix[0].length * side, matrix.length * side);
    background('#acacac');
   

    for (var y = 0; y < matrix.length; ++y) {
        for (var x = 0; x < matrix[y].length; ++x) {
            if (matrix[y][x] == 1) {
                var gr = new Grass(x, y);
                grassArr.push(gr);
            }
            else if (matrix[y][x] == 2) {
                var xotaker = new Xotaker(x, y)
                xotakerArr.push(xotaker)
            }
            else if (matrix[y][x] == 3) {
                var norgishatich = new gishatich(x, y)
                gishatichArr.push(norgishatich)
            }
            else if (matrix[y][x] == 4) {
                var norvorsord = new vorsord(x,y);
                vorsordArr.push(norvorsord);
            }
            else if(matrix[y][x] == 5){
                var nordjoxq = new Djoxq(x,y);
                djoxqArr.push(nordjoxq);
            }
        }
    }


}
setInterval(changecol,3000);

function changecol(){
    
    weather++;
    if(weather == 4){
        weather = 0;
    }
}

function draw() {

    for (var y = 0; y < matrix.length; y++) {
        for (var x = 0; x < matrix[y].length; x++) {

            if (matrix[y][x] == 1) {
                if(weather == 0){
                    fill(205, 239, 242);
                }
                else if(weather == 1){
                    fill(191, 244, 66);
                }
                else if(weather == 2){
                    fill(106, 209, 27);
                }
                else if(weather == 3){
                    fill('orange');
                }
                else{
                    fill(205, 239, 242);
                }
               
            }
            else if (matrix[y][x] == 0) {
                fill("#acacac");
              
            }
            else if (matrix[y][x] == 2) {
                fill("yellow");
              
            }
            else if (matrix[y][x] == 3) {
                fill("red");
              
            }
            else if (matrix[y][x] == 4) {
                fill("black");
                          }
            else if(matrix[y][x] == 5){
                fill("blue");
              
            }

            rect(x * side, y * side, side, side);
        }
    }

    for (var i in grassArr) {
        grassArr[i].bazmanal();
    }

     for (var i in xotakerArr) {
         
        xotakerArr[i].utel();
     }

     
     for(var i in gishatichArr){
       gishatichArr[i].utel();
     }

     
     for(var i in vorsordArr){
         vorsordArr[i].sharjvel();
         vorsordArr[i].utel();
         vorsordArr[i].bazmanal();
         vorsordArr[i].mahanal();
     }
      for(var i in djoxqArr){
         djoxqArr[i].sharjvel();
         djoxqArr[i].utel();
     }
     
}


